package com.lti.model;

public class Ticket {
	
	private double price;
	private int ticketID;
	
	Ticket(){
		
	}
	
	
	public double getPrice() {
		return price;
	}
	public void setPrice(double price) {
		this.price = price;
	}
	public int getTicketID() {
		return ticketID;
	}
	public void setTicketID(int ticketID) {
		this.ticketID = ticketID;
	}
	
	

}
